def run_visualiser(video_path, side="right"):
    from google.colab.patches import cv2_imshow # Added import here
    indices = LANDMARK_INDICES[side]

    base_options = python.BaseOptions(model_asset_path=MODEL_PATH)
    #Configure the loaded model and set parameters for inference
    options = vision.PoseLandmarkerOptions(
        base_options               = base_options, #The model is attached
        running_mode               = vision.RunningMode.VIDEO,   #Mediapipe Tasks supports multiple modes - image, video and live stream. Therefore, here we define the mode we want to operate in - which is the video mode.
        #During video processing (frame by frame processing), there is temporal tracking so the model keeps track of (or remembers) the previous frame
        num_poses                  = 1, #The main difference between this and solutions is that tasks can detect more than one people in the frame. Here, since we have set num_poses as 1 it will detect the person that gives the best confidence.
        min_pose_detection_confidence = 0.5, #Here, we define the minimum pose detection confidence, only detections with a confidence score equal to or above 0.5 will be considered, others will be ignored
        min_pose_presence_confidence  = 0.5, #This parameter checks that after the detection of a person, how confident is it that the pose landmarks (joint coordinates) are actually present and reliable enough to use
        min_tracking_confidence       = 0.5, #This parameter answers the question I knew where the body landmarks were in the previous frame. How confident am I that I can keep following (tracking) those same landmarks correctly in the current frame?
    ) #If we are processing a video fo 30fps running pose detection on every frame would be expensive so what mediapipe does is - it runs the full pose detection model on the first frame and then for the next frames it only tracks the joint coordinates since it has a context of the previous joint coordinates.
    """ Instead of asking every frame - where the person is. It already knows where the knee was in the last frame, it now checks where did it move now. What is actually happening here - Previous frame + motion estimate -> predict next landmark position.
    The tracker predicts then compares the prediction with the actual image features. If the prediction matches the image then the tracking confidence is high.
    In case the tracking confidence is too low, mediapipe will stop tracking and start detecting again from scratch from the same frame. """
    """ Mediapipe processes frames in the following manner:
    Stage 1 -> Detect a person
    Stage 2 -> Estimate body landmarks
    Stage 3 -> Track landmarks in the next frame

    The above three confidence parameters map to those stages:
    Stage 1 -> min_pose_detection_confidence
    Stage 2 -> min_pose_presence_confidence
    Stage 3 -> min_tracking_confidence
    min_pose_detection_confidence only helps detect a person, not if the joint coordinates can be detected accurately or not. It might be possible that a person might have a detection confidence of 0.8 but since a few joints are partially hidden
    min_pose_presence_confidence might be below the threshold of 0.5 and hence will be rejected. """


#Basic properties are extracted such as the frames one by one, the fps rate, the width and height of the image
    cap = cv2.VideoCapture(video_path)
    fps = cap.get(cv2.CAP_PROP_FPS) or 30.0
    w   = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    h   = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    print(f"\nVideo: {os.path.basename(video_path)}")
    print(f"Resolution: {w}x{h}  |  FPS: {fps}")
    print("\nControls:")
    print("  SPACE → pause / resume")
    print("  Q     → quit")
    print("  S     → save current frame as screenshot")
    print("  F     → slow motion (half speed)")
    print("  D     → fast forward (double speed)\n")

    #These variables control the video playback
    paused     = False #Keeps track of whether the video playback is paused or not. Initially the video is playing
    slow       = False #Tracks whether slow motion mode is active. False = normal playback speed
    fast       = False #Tracks whether fast forward mode is active. Initially fast forward mode is not active
    frame_idx  = 0 #Initially at frame 0
    delay      = int(1000 / fps) #1000/30 - 33 milli seconds. Therefore the next frame is shown after every 33 milli seconds

    with vision.PoseLandmarker.create_from_options(options) as detector:
        while cap.isOpened():
            if not paused: #proces the next frame only if the video is not paused
                ret, frame = cap.read() #Each frame is read
                if not ret: #If no frame is returned it means the video has ended so we break out of the loop
                    break

                #Run MediaPipe
                rgb      = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB) #Convert bgr to rgb. OpenCV stores images in bgr format but mediapipe requires rgb format
                mp_image = mp.Image(image_format=mp.ImageFormat.SRGB, data=rgb) #Create an image object that stores attributes of data and functions that operate on the data
                ts_ms    = int(frame_idx * 1000 / fps) #Time stamps are required for mediapipe, which are obtained by converting fps
                result   = detector.detect_for_video(mp_image, ts_ms) #The pose detection model is loaded her and provided an input of the image object and timestamp of the frame

                if result.pose_landmarks and len(result.pose_landmarks) > 0:
                    lms = result.pose_landmarks[0] #Store the landmarks of the first person (only person)

                    #Full skeleton
                    draw_skeleton(frame, lms, w, h)

                    #Key joints highlighted
                    coords = draw_key_joints(frame, lms, indices, w, h)

                    #Angle values
                    knee_angle, hip_angle, ankle_angle = draw_angles(frame, coords)

                    #Visibility scores for key joints
                    hip_vis   = lms[indices["hip"]].visibility
                    knee_vis  = lms[indices["knee"]].visibility
                    ankle_vis = lms[indices["ankle"]].visibility

                    #Displays detected status on the top left corner
                    cv2.putText(frame, "DETECTED", (20, 40),
                                cv2.FONT_HERSHEY_SIMPLEX, 1,
                                DETECTED_COLOUR, 2)

                    #Visibility scores bottom left
                    cv2.putText(frame,
                        f"Visibility — Hip:{hip_vis:.2f}  Knee:{knee_vis:.2f}  Ankle:{ankle_vis:.2f}",
                        (20, h - 20),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.55, (200, 200, 200), 1)

                    #If no person found
                else:
                    cv2.putText(frame, "NOT DETECTED", (20, 40),
                                cv2.FONT_HERSHEY_SIMPLEX, 1,
                                MISSING_COLOUR, 2)

                #Frame counter top right
                cv2.putText(frame, f"Frame: {frame_idx}",
                            (w - 180, 40),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.7, (200, 200, 200), 2)

                frame_idx += 1

            cv2_imshow(frame) # Replaced cv2.imshow with cv2_imshow

            #Adjust playback speed. If normal speed then wait = delay, if video speed is slow then wait = delay * 2 and if video speed is fast then wait = delay / 2
            wait = delay * 2 if slow else (delay // 2 if fast else delay)
            key  = cv2.waitKey(wait) & 0xFF #Read keyboard input

            if key == ord('q'): #Stop
                break
            elif key == ord(' '):
                paused = not paused
            elif key == ord('s'):
                fname = f"screenshot_frame_{frame_idx}.jpg"
                cv2.imwrite(fname, frame)
                print(f"Screenshot saved → {fname}")
            elif key == ord('f'):
                slow = False
                fast = not fast
                print("Fast forward" if fast else "Normal speed")
            elif key == ord('d'):
                fast = False
                slow = not slow
                print("Slow motion" if slow else "Normal speed")

    cap.release()
    print("Done.")
#Run the main visualizer
run_visualiser(VIDEO_PATH, SIDE)