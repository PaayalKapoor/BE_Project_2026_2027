def draw_skeleton(frame, landmarks, w, h):
    """Draw full MediaPipe pose skeleton manually — compatible with Tasks API."""

    # MediaPipe's 35 pose connections defined manually
    POSE_CONNECTIONS = [
        (0, 1), (1, 2), (2, 3), (3, 7),       # face left
        (0, 4), (4, 5), (5, 6), (6, 8),        # face right
        (9, 10),                                 # mouth
        (11, 12),                                # shoulders
        (11, 13), (13, 15),                      # left arm
        (12, 14), (14, 16),                      # right arm
        (15, 17), (15, 19), (17, 19),            # left hand
        (16, 18), (16, 20), (18, 20),            # right hand
        (11, 23), (12, 24),                      # torso sides
        (23, 24),                                # hips
        (23, 25), (25, 27), (27, 29), (27, 31), # left leg
        (24, 26), (26, 28), (28, 30), (28, 32), # right leg
        (29, 31), (30, 32),                      # feet
    ]

    # Draw all connections
    for start_idx, end_idx in POSE_CONNECTIONS:
        if start_idx < len(landmarks) and end_idx < len(landmarks):
            lm_start = landmarks[start_idx]
            lm_end   = landmarks[end_idx]

            # Only draw if both landmarks have reasonable visibility
            if lm_start.visibility > 0.3 and lm_end.visibility > 0.3:
                px_start = (int(lm_start.x * w), int(lm_start.y * h))
                px_end   = (int(lm_end.x * w),   int(lm_end.y * h))
                cv2.line(frame, px_start, px_end, SKELETON_COLOUR, 2)

    # Draw all landmark dots
    for lm in landmarks:
        if lm.visibility > 0.3:
            px = int(lm.x * w)
            py = int(lm.y * h)
            cv2.circle(frame, (px, py), 3, SKELETON_COLOUR, -1)

def draw_key_joints(frame, lms, indices, w, h): #Input is the current frame, the list of landmarks detected by mediapipe, indices tells us the landmarks which we actually want and the width and height of the frame.
    """Highlight the 5 key joints in red with labels."""
    joint_names = { #Give labels to each joint
        "shoulder": "SHO",
        "hip":      "HIP",
        "knee":     "KNEE",
        "ankle":    "ANK",
        "foot":     "FOOT",
    }
    coords = {}
    for joint, idx in indices.items(): #We loop through 5 joints
        px, py = landmark_to_pixel(lms[idx], w, h) #Conver the normalized coordinates to pixel coordinates for drawing
        coords[joint] = (px, py) #Store the coordinates in a dictionary.
        cv2.circle(frame, (px, py), 8, KEY_JOINT_COLOUR, -1) #This draws a filled circle on the landmark. cv2.circle(image, center, radius, color, thickness)
        cv2.putText(frame, joint_names[joint], (px + 10, py - 10),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.55, KEY_JOINT_COLOUR, 2) #Set the label close to the circle drawn
    return coords

def draw_angles(frame, coords):
    """Calculate and display the 3 key angles next to their joints."""
    s  = coords["shoulder"]
    h  = coords["hip"]
    k  = coords["knee"]
    a  = coords["ankle"]
    f  = coords["foot"]

    knee_angle  = calculate_angle(h, k, a)
    hip_angle   = calculate_angle(s, h, k)
    ankle_angle = calculate_angle(k, a, f)

    #Draw angle values next to each joint. cv2.putText(image, text, position, font, size, color, thickness)
    cv2.putText(frame, f"Knee: {knee_angle:.1f}",
                (k[0] - 120, k[1]), #k is the knee coorindate - (640, 320) so this represents the position where the angle should be displayed. k[0] = 640, k[1] = 320
                cv2.FONT_HERSHEY_SIMPLEX, 0.65, ANGLE_COLOUR, 2)
    cv2.putText(frame, f"Hip: {hip_angle:.1f}",
                (h[0] - 120, h[1]),
                cv2.FONT_HERSHEY_SIMPLEX, 0.65, ANGLE_COLOUR, 2)
    cv2.putText(frame, f"Ankle: {ankle_angle:.1f}",
                (a[0] - 120, a[1]),
                cv2.FONT_HERSHEY_SIMPLEX, 0.65, ANGLE_COLOUR, 2)

    return knee_angle, hip_angle, ankle_angle

