#Angles are calculated using the three point formula. #Currently the dataframe contains raw joint coordinates, we need joint angles therefore we calculate them using trignometry
##Here we calculate the angle formed by three points, the angle is calculated at point B.
def calculate_angle(a, b, c):
    a, b, c = np.array(a), np.array(b), np.array(c) ##In the following step we get two vectors. Formula to get the angle - Cos theta = (ba.bc)/(|ba||bc|)
    ba = a - b
    bc = c - b
    denom = np.linalg.norm(ba) * np.linalg.norm(bc) + 1e-8
    cos_val = np.dot(ba, bc) / denom
    return float(np.degrees(np.arccos(np.clip(cos_val, -1.0, 1.0))))


